#include <stdio.h>

int main() {

    int num1 = 5, num2 = 7;
    printf("num1 + num2 = %d\n", num1+num2);
    printf("num1 - num2 = %d\n", num1-num2);
    printf("num1 * num2 = %d\n", num1*num2);
    printf("num1 / num2 = %d\n", num1/num2);
    printf("num1 %% num2 = %d\n", num1%num2);

    return 0;
}